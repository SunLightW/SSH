package cn.jasmine.entity.admin;


public class Hr {

  private long id;
  private String hrTitle;
  private long hrNum;
  private String hrBody;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getHrTitle() {
    return hrTitle;
  }

  public void setHrTitle(String hrTitle) {
    this.hrTitle = hrTitle;
  }


  public long getHrNum() {
    return hrNum;
  }

  public void setHrNum(long hrNum) {
    this.hrNum = hrNum;
  }


  public String getHrBody() {
    return hrBody;
  }

  public void setHrBody(String hrBody) {
    this.hrBody = hrBody;
  }

}
